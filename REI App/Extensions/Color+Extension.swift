import SwiftUI

// Color extension for named colors in Assets
extension Color {
    static let appPrimary = Color("PrimaryColor")
    static let appSecondary = Color("SecondaryColor")
    static let appAccent = Color("AccentColor")
    static let appBackground = Color("BackgroundColor")
    static let appSecondaryBackground = Color("SecondaryBackgroundColor")
    static let appPrimaryText = Color("PrimaryTextColor")
    static let appSecondaryText = Color("SecondaryTextColor")
} 